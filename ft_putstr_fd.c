#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	x;

	x = 0;
	if (!s)
		return ;
	if (fd >= 0)
	{
		while (s[x])
			ft_putchar_fd(s[x++], fd);
	}
}
